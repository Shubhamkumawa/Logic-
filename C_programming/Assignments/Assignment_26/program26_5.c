#include<stdio.h>

void Pattern(int ino)
{
    int iCnt  = 0;
    for(iCnt=1; iCnt<=ino; iCnt++)
    {
       printf("%d\t",iCnt*2);
    }
}
int main()
{
    int ivalue = 0;

    printf("Enter a number : \n");
    scanf("%d",&ivalue);

    Pattern(ivalue);

    return 0;
}

